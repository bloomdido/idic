export const prefixProp = (prefix, prop) => {
  return prefix + prop.replace(/^\w/, c => c.toUpperCase())
}

export const rootProp = (prefix, prop) => {
  return prop.replace(prefix, "").replace(/^\w/, c => c.toLowerCase())
}

